export function evaluate(S) {
  const rows = [];
  let sc = 0, mx = 0, hard = null;
  const put = (lvl, k, v) => rows.push({ lvl, k, v });

  mx += 14;
  if (S.age >= 29 && S.age <= 43) { sc += 14; put('ok', 'возраст', 'в диапазоне 29–43'); }
  else if (S.age >= 26 && S.age <= 46) { sc += 9; put('warn', 'возраст', 'рядом с диапазоном — обсуждаемо'); }
  else { sc += 4; put('warn', 'возраст', 'вне предпочтений, но не стоп'); }

  mx += 8;
  if (S.hgt >= 168) { sc += 8; put('ok', 'рост', '168 см и выше'); }
  else if (S.hgt >= 163) { sc += 5; put('warn', 'рост', 'чуть ниже предпочтения'); }
  else { sc += 2; put('warn', 'рост', 'ниже предпочтения — не критично'); }

  mx += 18;
  if (S.kids === 'none') { sc += 18; put('ok', 'дети', 'нет — совпадает'); }
  else if (S.kids === 'adult') { sc += 16; put('ok', 'дети', 'взрослые — подходит'); }
  else { put('bad', 'дети', 'несовершеннолетние — не рассматривается'); hard = 'kids'; }

  mx += 22;
  if (S.goal === 'family') { sc += 22; put('ok', 'цель', 'семья и брак — полное совпадение'); }
  else if (S.goal === 'maybe') { sc += 10; put('warn', 'цель', '«посмотрим» — стоит обсудить прямо'); }
  else { put('bad', 'цель', 'без обязательств — расходимся'); hard = 'goal'; }

  mx += 16;
  if (S.work === 'remote') { sc += 16; put('ok', 'работа', 'профессия онлайн — идеально'); }
  else if (S.work === 'office') { sc += 13; put('ok', 'работа', 'своя профессия — принимается'); }
  else if (S.work === 'pause') { sc += 11; put('ok', 'работа', 'временная пауза — нормально'); }
  else { put('bad', 'работа', 'отказ работать в принципе — неприемлемо'); hard = 'work'; }

  mx += 20;
  if (S.eng === 'fluent') { sc += 20; put('ok', 'английский', 'свободно — совпадает'); }
  else if (S.eng === 'good') { sc += 18; put('ok', 'английский', 'хороший уровень — достаточно'); }
  else if (S.eng === 'basic') { sc += 3; put('warn', 'английский', 'базовый — ниже требуемого'); }
  else { put('bad', 'английский', 'без языка — неприемлемо'); hard = 'eng'; }

  mx += 16;
  if (S.edu === 'phd' || S.edu === 'master') { sc += 16; put('ok', 'образование', 'высшее сверх минимума — совпадает'); }
  else if (S.edu === 'bachelor') { sc += 15; put('ok', 'образование', 'бакалавр — минимум выполнен'); }
  else if (S.edu === 'college') { sc += 5; put('warn', 'образование', 'неполное / колледж — ниже требуемого'); }
  else { put('bad', 'образование', 'без высшего — принципиальное несовпадение'); hard = 'edu'; }

  mx += 8;
  if (S.read === 'lots') { sc += 8; put('ok', 'развитие', 'любит читать и расти — совпадает'); }
  else if (S.read === 'some') { sc += 5; put('ok', 'развитие', 'читает иногда — нормально'); }
  else { sc += 1; put('warn', 'развитие', 'без чтения и роста — расхождение'); }

  mx += 10;
  if (S.soc === 'low') { sc += 10; put('ok', 'соцсети', 'приватность — совпадение'); }
  else if (S.soc === 'mid') { sc += 7; put('ok', 'соцсети', 'умеренно — нормально'); }
  else { sc += 1; put('warn', 'соцсети', 'жизнь напоказ — расхождение'); }

  mx += 10;
  if (S.move === 'yes') { sc += 10; put('ok', 'мобильность', 'готовность к переезду'); }
  else if (S.move === 'maybe') { sc += 7; put('ok', 'мобильность', 'обсуждаемо'); }
  else { sc += 2; put('warn', 'мобильность', 'привязка к месту — усложняет'); }

  mx += 4;
  if (S.smoke === 'no') { sc += 4; put('ok', 'привычки', 'не курит — совпадает'); }
  else if (S.smoke === 'some') { sc += 2; put('warn', 'привычки', 'иногда — не идеально'); }
  else { put('warn', 'привычки', 'курение — расхождение'); }

  const raw = Math.round((sc / mx) * 100);
  const pct = hard ? Math.min(28, raw) : raw;

  let verdict;
  if (hard === 'kids') verdict = 'Здесь мы честно расходимся. Я не хочу заново входить в роль отчима и брать воспитание несовершеннолетних детей — это не про их качества, а про мой осознанный выбор.';
  else if (hard === 'goal') verdict = 'Тоже честно: я ищу семью, а не формат «без обязательств». Лучше выяснить это в первую минуту, чем через полгода.';
  else if (hard === 'work') verdict = 'Мой единственный пункт без исключений. Дело не в деньгах — мне нужен человек со своей жизнью и своим смыслом дня. Когда его нет, вся энергия уходит на партнёра: контроль, придирки, претензии. Пауза на декрет, учёбу или переезд — совсем другое дело.';
  else if (hard === 'eng') verdict = 'Без английского не получится: мы живём между странами. Партнёрша окажется в изоляции, а я — постоянным переводчиком. Если язык в процессе — напишите, это решаемо за год работы.';
  else if (hard === 'edu') verdict = 'Здесь принципиально: мне нужна образованная женщина — минимум бакалавр — и человек, который любит читать и развиваться. Дело не в самом дипломе, а в складе ума за ним. Если высшее ещё в процессе — напишите, это другое дело.';
  else if (pct >= 85) verdict = 'Совпадение по всем ключевым осям. Дальше решают вещи, которые не поместились ни в один слайдер: голос, юмор, взгляд и поведение, когда трудно. Напишите — узнаем.';
  else if (pct >= 65) verdict = 'Хорошая основа. Есть пара пунктов для откровенного разговора — и это нормально: взрослые люди договариваются, а не подгоняют друг друга под таблицу.';
  else verdict = 'По формальным параметрам расходимся во многом. Но параметры — рамка, а не приговор: если вам близко написанное выше, цифры я готов пересчитать.';

  return { rows, pct, hard, verdict };
}
